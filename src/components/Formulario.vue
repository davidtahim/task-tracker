<template>
    <div class="main">
        <div class="content" role="form" aria-label="Formulário de tarefas">
            <form @submit.prevent="onSubmit">
                <div class="mb-3">
                    <label for="taskText" class="form-label">Tarefa</label>
                    <input
                        type="text"
                        id="taskText"
                        v-model="text"
                        class="form-control"
                        placeholder="Adicionar tarefa"
                        required
                        aria-required="true"
                    />
                </div>
                <div class="mb-3 form-check">
                    <input
                        type="checkbox"
                        id="taskReminder"
                        v-model="reminder"
                        class="form-check-input"
                    />
                    <label for="taskReminder" class="form-check-label">Definir lembrete</label>
                </div>
                <button type="submit" class="btn btn-primary">Salvar Tarefa</button>
            </form>
            <div class="col-md-4">
                <section>
                    <strong>
                        {{ //tempoEmSegundos 
                         tempoDecorrido 
                        }}
                    </strong>
                </section>
            <button class="button" @click="iniciar">
            <span class="icon">
                <i class="fas fa-play"></i>
            </span>
            <span>play</span>
        </button>
        <button class="button" @click="finalizar">
            <span class="icon">
                <i class="fas fa-stop"></i>
            </span>
            <span>stop</span>
        </button>    
            </div>
        </div>
    </div>
</template>

<script>
import { defineComponent } from 'vue';

export default defineComponent({
  name: 'Formulario',
  data() {
    return {
      tempoEmSegundos: 0,
      cronometro: 0,  
      text: '',
      reminder: false
    }
  },
  computed: {
    // Adicione propriedades computadas aqui, se necessário
    tempoDecorrido() {
      return new Date(this.tempoEmSegundos * 1000).toISOString().substr(11, 8);  
      
    }
  },
  methods: {
    iniciar() {
      // Lógica de inicialização, se necessário
     this.cronometro = setInterval(() => {
        this.tempoEmSegundos+=1;
      }, 1000);
      
      
    },
    finalizar(){
        // Lógica de finalização, se necessário
        clearInterval(this.cronometro);
    },
    onSubmit() {
      // Adicione aqui a lógica para lidar com o envio do formulário
      // Por exemplo, emitir um evento ou atualizar um array de tarefas
      // Exemplo:
      this.$emit('add-task', {
        text: this.text,
        reminder: this.reminder
      });
      // Limpar o formulário após o envio
      this.text = '';
      this.reminder = false;
    }
  }
});
</script>